import { cinemas, Cinema } from "@/data/cinemas";

export function getDistance(lat1: number, lng1: number, lat2: number, lng2: number) {
  // Haversine formula
  const R = 6371; // km
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1 * Math.PI / 180) *
      Math.cos(lat2 * Math.PI / 180) *
      Math.sin(dLng / 2) *
      Math.sin(dLng / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

export function findNearestCinema(userLat: number, userLng: number): Cinema | null {
  let minDist = Infinity;
  let nearest: Cinema | null = null;
  for (const cinema of cinemas) {
    const dist = getDistance(userLat, userLng, cinema.lat, cinema.lng);
    if (dist < minDist) {
      minDist = dist;
      nearest = cinema;
    }
  }
  return nearest;
}